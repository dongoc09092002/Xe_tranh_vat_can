//*******************cac trang thai cua xe(car)**********************
typedef enum{
	CAR_STATE_STOP,
	CAR_STATE_LEFT,
	CAR_STATE_RIGHT,
	CAR_STATE_BACK,
	CAR_STATE_FORDWARD,
	CAR_STATE_BACK_RIGHT,
}CAR_STATE ;


void car_control(CAR_STATE car_state , uint8_t speed){
	
	switch(car_state){
		case CAR_STATE_STOP:
			motor_control(&motor_left,STATE_STOP,0);
			motor_control(&motor_right,STATE_STOP,0);
			break;
		case CAR_STATE_LEFT: 
			motor_control(&motor_left,STATE_STOP,0);
			motor_control(&motor_right,STATE_FORWARD,speed);
			break;
		case CAR_STATE_RIGHT: 
			motor_control(&motor_right,STATE_STOP,0);
			motor_control(&motor_left,STATE_FORWARD,speed);
			break;
		case CAR_STATE_BACK: 
			motor_control(&motor_left,STATE_BEHIND,speed);
			motor_control(&motor_right,STATE_BEHIND,speed);
			break;
		case CAR_STATE_FORDWARD: 
			motor_control(&motor_left,STATE_FORWARD,speed);
			motor_control(&motor_right,STATE_FORWARD,speed);
			break;
		case CAR_STATE_BACK_RIGHT: 
			motor_control(&motor_right,STATE_STOP,0);
			motor_control(&motor_left,STATE_BEHIND,speed);
			break;
		default :
			break;
	}
}



